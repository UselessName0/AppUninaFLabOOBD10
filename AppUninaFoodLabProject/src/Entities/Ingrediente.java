package Entities;

public class Ingrediente {
	
	//ATTRIBUTI
	private String Nome;
	
	//COSTRUTTORI
	public Ingrediente(String Nome) {
		this.Nome = Nome;
	}
	
	//METODI
		//GETTER AND SETTER
		public String getNome() {
			return Nome;
		}
		public void setNome(String nome) {
			Nome = nome;
		}
			
}
